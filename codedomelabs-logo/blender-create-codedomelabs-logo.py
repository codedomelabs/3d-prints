#  CAPSULE BUILDER  (single-wall surface, NOT hollow)
# ═══════════════════════════════════════════════════════════════

def build_capsule(name, height, radius, cap_h, segments):
    cyl_half = (height - 2.0 * cap_h) / 2.0
    assert cyl_half >= 0

    arc_n = max(8, segments // 4)
    cyl_n = max(4, segments // 4)

    bm = bmesh.new()
    pts = []

    pts.append(bm.verts.new((0.0, 0.0, cyl_half + cap_h)))
    for i in range(1, arc_n + 1):
        t = (math.pi / 2.0) * i / arc_n
        pts.append(bm.verts.new((radius * math.sin(t), 0.0,
                                  cyl_half + cap_h * math.cos(t))))
    for i in range(1, cyl_n):
        pts.append(bm.verts.new((radius, 0.0,
                                  cyl_half * (1.0 - 2.0 * i / cyl_n))))
    for i in range(0, arc_n + 1):
        t = (math.pi / 2.0) * i / arc_n
        pts.append(bm.verts.new((radius * math.cos(t), 0.0,
                                  -cyl_half - cap_h * math.sin(t))))

    edges = [bm.edges.new((pts[i], pts[i+1])) for i in range(len(pts)-1)]

    bmesh.ops.spin(
        bm, geom=edges + pts,
        axis=(0, 0, 1), cent=(0, 0, 0), dvec=(0, 0, 0),
        angle=math.pi * 2.0, steps=segments,
        use_merge=True, use_normal_flip=False, use_duplicate=False,
    )
    bmesh.ops.remove_doubles(bm, verts=bm.verts, dist=0.001)
    bmesh.ops.recalc_face_normals(bm, faces=bm.faces)

    mesh = bpy.data.meshes.new(name)
    bm.to_mesh(mesh)
    mesh.update()
    bm.free()

    obj = bpy.data.objects.new(name, mesh)
    bpy.context.collection.objects.link(obj)
    return obj


