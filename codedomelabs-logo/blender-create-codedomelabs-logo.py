#  POINT-IN-POLYGON
# ═══════════════════════════════════════════════════════════════

def point_in_polygon(x, z, poly):
    n = len(poly)
    inside = False
    j = n - 1
    for i in range(n):
        xi, zi = poly[i]
        xj, zj = poly[j]
        if ((zi > z) != (zj > z)) and \
           (x < (xj - xi) * (z - zi) / (zj - zi) + xi):
            inside = not inside
        j = i
    return inside


def classify_face(x, z):
    is_blue = (point_in_polygon(x, z, BLUE_RED_MAIN) or
               point_in_polygon(x, z, BLUE_RED_TONGUE))
    if is_blue:
        if point_in_polygon(x, z, NAVY_BOUNDARY):
            return "dark_navy"
        return "royal_blue"
    else:
        if point_in_polygon(x, z, MAROON_BOUNDARY):
            return "dark_maroon"
        return "bright_red"


